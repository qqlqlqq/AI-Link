package com.quan.ailink.entity;

public class DepartmentUser {
    private Long duid;

    private Long uid;

    private Long did;

    private Long rid;

    public Long getDuid() {
        return duid;
    }

    public void setDuid(Long duid) {
        this.duid = duid;
    }

    public Long getUid() {
        return uid;
    }

    public void setUid(Long uid) {
        this.uid = uid;
    }

    public Long getDid() {
        return did;
    }

    public void setDid(Long did) {
        this.did = did;
    }

    public Long getRid() {
        return rid;
    }

    public void setRid(Long rid) {
        this.rid = rid;
    }
}